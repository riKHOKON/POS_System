    private void add_btnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_add_btnActionPerformed
        PreparedStatement pst = null;
       Connection con = null;
       ResultSet rs = null;
       try {
                con = DbConnector.ConnectDB();
                boolean isContain = false;
                String check = "SELECT * FROM companies WHERE Name = '"+com_txt.getText()+"'";              
                pst = con.prepareStatement(check);
                rs = pst.executeQuery();
                if (rs.next()) {
                    isContain = true;
                    JOptionPane.showMessageDialog(null, com_txt.getText()+" already exists.\nYou can't insert it.");
                    com_txt.setText("");
                }else{
                    if(!com_txt.getText().equals("")){
                        int value = 0;
                        pst = null;
                        int i = 0;
                        value = JOptionPane.showConfirmDialog(null, "Do you really want to add!","Add",JOptionPane.YES_NO_OPTION);
                        if (value == 0) {
                            do {
                                pst = con.prepareStatement("INSERT INTO companies (Name) VALUES (?)");
                                pst.setString(1,com_txt.getText());
                                pst.executeUpdate();
                                com_txt.setText("");
                                JOptionPane.showMessageDialog(null, "Company added successfully.");
                                i++;
                            } while (i < 1 && isContain == false);   
                        }
                    }else{
                        JOptionPane.showMessageDialog(null, "Insert A Name. Then press add button.");
                    }
                } 
            } catch (SQLException e) {
                JOptionPane.showMessageDialog(null, e.getMessage());
            }
            finally{
                try {
                    rs.close();
                    pst.close();
                    con.close();
                } catch (SQLException ex) {
                   // Logger.getLogger(UserEditingForm.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
    }//GEN-LAST:event_add_btnActionPerformed

