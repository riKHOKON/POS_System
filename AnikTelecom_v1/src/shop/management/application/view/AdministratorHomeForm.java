              model.setValueAt(bill.getBillItems().get(index).getSellingPrice(), index, 2);  
              model.setValueAt(bill.getBillItems().get(index).getQty(), index, 3);
              model.setValueAt(bill.getBillItems().get(index).getTotalPrice(), index, 4);
