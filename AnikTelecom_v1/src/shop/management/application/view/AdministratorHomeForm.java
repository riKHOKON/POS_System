            int qty = Integer.parseInt(billItemQty_txt.getText());
            bill.getBillItems().get(index).setSellingPrice(price);
            bill.getBillItems().get(index).setQty(qty);
            bill.getBillItems().get(index).setTotalPrice(price, qty);
